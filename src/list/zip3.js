// zip3 :: [a] -> [b] -> [c] -> [(a, b, c)]
const zip3 = ([x, ...xs]) => ([y, ...ys]) => ([z, ...zs]) =>
    x === undefined
        ? []
        : y === undefined
            ? []
            : z === undefined
                ? []
                : [[x,y,z], ...zip3(xs)(ys)(zs)]
;

export default zip3



console.log(
    '\n',
    zip3 ( []        ) ( []        ) ([]         ), '\n',
    zip3 ( []        ) ( []        ) ([7,88,9,66]), '\n',
    zip3 ( []        ) ( [2,4,6,8] ) ([7,88,9,66]), '\n',
    zip3 ( [1,3,5,7] ) ( [2,4]     ) ([         ]), '\n',

    zip3 ( [1,3,5,7] ) ( [2,4,6,8] ) ([7,88,9,66]), '\n',
    zip3 ( [1,3,5,7] ) ( [2,4]     ) ([7,88,9,66]), '\n',

    zip3 ( [true, false]) ( ['a', 'b', 'c'] ) ( [1, 2, 3, 4] ), '\n',
)
