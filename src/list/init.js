// ----------------------------------------------------------------- definition
// init :: [a] -> [a]
const init = ([y, x, ...xs]) =>
    x === undefined
        ? []
        : [y, x, ...init(xs)]
;


// --------------------------------------------------------------------- export
export default init;

// ----------------------------------------------------------------- invariants


// ---------------------------------------------------------------------- check
console.log('\n',
    init ('cobra')                  , '\n',
    init ([])                  , '\n',
    init ([12])                  , '\n',
    init ([12,13])                  , '\n',
    init ([1,2,1,3])                  , '\n',
)




