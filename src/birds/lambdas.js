// --------------------------------------------------------------------- object
const λ = {};

// -------------------------------------------------------------------- methods
// λg.λf.λx.λy.gxy(fxy)
λ.B  = g => f => x => g(f(x))
// λg.λf.λx.λy.gxy(fxy) ≡ BBB
λ.B1 = g => f => x => y => g(x)(y) (f(x)(y))

λ.C  = f => a => b => f(b, a)
λ.I  = x => x
λ.K  = a => _ => a
λ.KI = _ => b => b
// λg.λf.λx.gx(fx)
λ.S = f => g => x => f(x) (g(x))

λ.ω = f => f(f)

// Y := λf.(λx.f(   xx ))(λx.f(   xx ))
λ.Y = f => (x => f(x(x))) (x => f(x(x)))

// Z := λf.(λx.f(λv.xxv))(λx.f(λv.xxv))
λ.Z = f => (x => f( (() => x(x)) () )) (x => f( (() => x(x)) () ))

// -------------------------------------------------------------------- aliases
λ.T = λ.K;
λ.F = λ.KI;
λ.Z = λ.KI;


// ---------------------------------------------------------------------- check
